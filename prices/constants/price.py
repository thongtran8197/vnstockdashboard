class TimeRangeType:
    YEARLY = 1
    QUARTERLY = 2

    @classmethod
    def to_text(cls, time_range_type: int) -> str:
        chart_type_text = ""
        if time_range_type == cls.YEARLY:
            chart_type_text = "Yearly"
        elif time_range_type == cls.QUARTERLY:
            chart_type_text = "Quarterly"
        return chart_type_text

    @classmethod
    def is_yearly(cls, time_range_type: int) -> bool:
        return time_range_type == cls.YEARLY

    @classmethod
    def is_quarterly(cls, time_range_type: int) -> bool:
        return time_range_type == cls.QUARTERLY


